/**
 * Subagent 模块常量定义
 *
 * 集中管理所有 subagent 相关常量，消除魔法数字。
 */

// ============================================================
// 超时与时间相关
// ============================================================

/** 工具调用超时时间（7 分钟）
 *
 * 优化说明：
 * - 从 10 分钟减少到 7 分钟
 * - 避免死锁任务长时间占用资源
 * - 平衡用户体验和系统稳定性
 */
export const TOOL_TIMEOUT_MS = 7 * 60 * 1000;
// export const TOOL_TIMEOUT_MS = 10 * 1000;

/** LLM 调用超时时间（8 分钟）
 *
 * 每次 LLM 调用的最大等待时间，包括流式响应的完整过程。
 * - 避免 LLM API 响应卡住导致 subagent 永久等待
 * - 配合 withRetry 机制，瞬态错误会自动重试
 */
export const LLM_CALL_TIMEOUT_MS = 8 * 60 * 1000;

/** Subagent 总执行超时时间（10 分钟）
 *
 * 整个 subagent 从启动到结束的最大时间限制。
 * - 防止 subagent 因各种原因（循环、多步骤累积）无限执行
 * - 超时后会自动终止，释放资源
 * - 应大于单个 LLM 调用超时 × 预期步数
 */
// export const SUBAGENT_TOTAL_TIMEOUT_MS = 20 * 1000;
export const SUBAGENT_TOTAL_TIMEOUT_MS = 10 * 60 * 1000;

/** 用户操作等待超时时间（5 分钟）
 *
 * 当 subagent 失败后，等待用户选择 Retry/Stop 的最大时间。
 * - 从原来的 30 分钟缩短到 5 分钟
 * - 避免失败任务长时间占用并发槽位
 * - 超时后默认选择 Stop
 */
export const USER_ACTION_TIMEOUT_MS = 5 * 60 * 1000;

// ============================================================
// 并发与队列控制
// ============================================================

/** 最大同时运行的子代理数量
 *
 * 保守先设置为 3，避免过多子代理导致资源争抢和性能下降
 */
export const MAX_CONCURRENT_RUNNERS = 3;

/** 最大排队等待的任务数量
 *
 * 优化说明：
 * - 从 10 提升到 15，应对高并发场景
 * - 配合提升的并发数，减少任务被拒绝的概率
 */
export const MAX_QUEUE_SIZE = 15;

// ============================================================
// 执行限制
// ============================================================

/** 默认最大执行步数（Agent 未指定时的回退值） */
export const DEFAULT_MAX_STEPS = 10;

// ============================================================
// 事件与日志
// ============================================================

/** 环形缓冲最大事件数 */
export const MAX_EVENTS = 200;

// ============================================================
// 上下文压缩
// ============================================================

/** 上下文压缩触发阈值（已用 token 比例） */
export const COMPRESSION_THRESHOLD = 0.7;